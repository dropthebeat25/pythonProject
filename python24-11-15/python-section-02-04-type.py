'''

기본 데이터 타입

문자열(str): 문자열 저장
정수(int): 숫자를 저장
실수(float): 소수점 있는 숫자를 저장
불린(bool): 참/거짓 (True/False) 저장

'''


#문자열 타입
sentance = '족발 왔다'

print('문자열', sentance)
print('문자열 타입', type(sentance))

#숫자 타입
integer = 25
number = 25.7

print('숫자', integer)
print('숫자 타입', type(integer))
print('실수', number)
print('실수 타입', type(number))

#불린 타입

booldozer01 = True
booldozer02 = False

print('첫번째 불린', booldozer01)
print('첫번째 불린 타입', type(booldozer01))

print('두번째 불린', booldozer02)
print('두번째 불린 타입', type(booldozer02))